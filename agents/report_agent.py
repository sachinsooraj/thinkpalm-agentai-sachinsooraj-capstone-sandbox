class ReportAgent:
    """
    Agent 3: Generates post-mortem, executive summary, and alert payloads
    from the combined Triage + Remediation output.
    """

    def __init__(self, memory: MemoryStore):
        self.memory = memory

    def generate_report(self, incident: dict, diagnosis, plan) -> Generator:
        """Yields streaming events: building → sections → report → done."""

        sid = incident.get("id", "pod-crash")
        title = incident.get("title", "Infrastructure Incident")
        generated_at = datetime.utcnow().strftime("%Y-%m-%d %H:%M:%S UTC")

        yield {"type": "status", "msg": "📊 Report Agent generating post-mortem..."}
        time.sleep(0.4)

        yield {"type": "status", "msg": "✍️  Building executive summary..."}
        time.sleep(0.3)

        raw = MOCK_REPORTS.get(sid, {
            "executive_summary": f"Incident '{title}' triaged and remediated by InfraGPT. See findings below.",
            "timeline": ["Incident detected", "Triage complete", "Remediation plan generated"],
            "impact": "See triage findings for impact details.",
            "resolution_steps": plan.immediate_steps[:3] if plan else [],
            "slack_alert": f":rotating_light: *[InfraGPT]* {title} — {diagnosis.severity}",
            "pagerduty_payload": {"summary": title, "severity": diagnosis.severity.lower()},
        })

        yield {"type": "status", "msg": "📅 Assembling incident timeline..."}
        time.sleep(0.3)

        yield {"type": "status", "msg": "📣 Generating Slack & PagerDuty alerts..."}
        time.sleep(0.3)

        import uuid
        incident_id = f"INC-{uuid.uuid4().hex[:6].upper()}"

        report = IncidentReport(
            incident_id=incident_id,
            title=title,
            severity=diagnosis.severity,
            generated_at=generated_at,
            executive_summary=raw["executive_summary"],
            timeline=raw["timeline"],
            root_cause=diagnosis.root_cause,
            impact=raw["impact"],
            resolution_steps=raw["resolution_steps"],
            prevention=plan.prevention if plan else [],
            slack_alert=raw["slack_alert"],
            pagerduty_payload=raw["pagerduty_payload"],
        )

        yield {
            "type": "report",
            "report": report.to_dict(),
            "object": report,
            "slack_alert": report.slack_alert,
            "pagerduty_payload": report.pagerduty_payload,
            "timeline": report.timeline,
        }

        yield {"type": "status", "msg": f"✅ Report {incident_id} generated — ready for Loom / Confluence"}
