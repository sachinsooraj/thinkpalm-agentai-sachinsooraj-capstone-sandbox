"""
Report Agent — Agent 3 of InfraGPT

Receives both the TriageDiagnosis and RemediationPlan, then generates:
  1. Executive summary        (for stakeholders / Slack)
  2. Full post-mortem (RCA)   (structured incident report)
  3. PagerDuty / Slack alert  (copy-paste ready payload)
  4. Saves runbook entry      (to memory, for future agents)
"""

import time
from dataclasses import dataclass, field
from datetime import datetime
from typing import Generator
from memory.store import MemoryStore


@dataclass
class IncidentReport:
    incident_id: str
    title: str
    severity: str
    generated_at: str
    executive_summary: str
    timeline: list
    root_cause: str
    impact: str
    resolution_steps: list
    prevention: list
    slack_alert: str
    pagerduty_payload: dict

    def to_dict(self) -> dict:
        return {
            "incident_id": self.incident_id,
            "title": self.title,
            "severity": self.severity,
            "generated_at": self.generated_at,
            "executive_summary": self.executive_summary,
            "root_cause": self.root_cause,
            "impact": self.impact,
            "resolution_steps": self.resolution_steps,
            "prevention_count": len(self.prevention),
            "slack_alert": self.slack_alert,
        }


